package main

import "fmt"

func main()  {

	// tipe data integer
	fmt.Println(1 + 1)
	fmt.Println(4 - 4)

	//misalkan yang di bagi sampai hasil desimal (float)
	fmt.Println(8.0 / 3.0)
	fmt.Println(8 / 2)
	fmt.Println(3 * 3)
	fmt.Println(10 % 2)

	// logika boolean (&& = dan) (|| = atau) (! = not)
	fmt.Println(true && true)
	fmt.Println(true && false)
	fmt.Println(true || true)
	fmt.Println(true || false)
	fmt.Println(!true)


}

